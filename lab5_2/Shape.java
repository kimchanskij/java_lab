package lab5_2;
import java.awt.*;

public abstract class Shape {
    protected Color color;
    protected int x;
    protected int y;
    protected int width;
    protected int height;

    public Shape(Color color, int x, int y, int width, int height) {
        this.color = color;
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}